package pl.edu.pwr.myapplication;

public class DataTuple {
    public String date;
    public String steps;
    public String distance;
    public String speed;

    public DataTuple(String date, String steps, String distance, String speed) {
        this.date = date;
        this.steps = steps;
        this.distance = distance;
        this.speed = speed;
    }
}
